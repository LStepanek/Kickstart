## I am loading input data ----------------------------------------------------

my_input <- file.choose()
my_data <- readLines(my_input)


## ----------------------------------------------------------------------------

###############################################################################

## I am changing a working directory ------------------------------------------

setwd(
    gsub("(.*\\\\)(.*)", "\\1", my_input)
)


## ----------------------------------------------------------------------------

###############################################################################

## helper functions -----------------------------------------------------------

solveTheCase <- function(
    
    my_case
    
){
    
    # '''
    # Returns a solution for one case of the input data.
    # '''
    
    L <- as.integer(strsplit(my_case, split = " ")[[1]][1])
    R <- as.integer(strsplit(my_case, split = " ")[[1]][2])
    
    ## We can prove that possible number of balanced non-empty
    ## substrings will be maximal if the balanced string consisting of
    ## min(L, R) pairs of parentheses "(" and ")" would follow the form
    ## of "()()...()" such that there is exactly min(L, R) pairs of
    ## parentheses "()".
    
    ## The number of balanced non-empty substrings is then equal to
    ## $\frac{\min(L, R) \cdot (\min(L, R) + 1)}{2}$.
    
    return(min(L, R) * (min(L, R) + 1) / 2)
            
}


## ----------------------------------------------------------------------------

###############################################################################

## flow control ---------------------------------------------------------------

if(
    as.integer(my_data[1]) != length(my_data) - 1
){
    stop("Number of cases is fishy.", call. = FALSE)
}


## ----------------------------------------------------------------------------

###############################################################################

## core computations ----------------------------------------------------------

my_output <- NULL

for(i in 1:as.integer(my_data[1])){

    my_case <- my_data[i + 1]

    my_output <- c(
        my_output,
        paste(
            "Case #",
            i,
            ": ",
            solveTheCase(my_case),
            sep = ""
        )
    )
    
    flush.console()
    print(
        paste(
            "Process is ",
            format(
                round(
                    i / as.integer(my_data[1]) * 100,
                    digits = 2
                ),
                nsmall = 2
            ),
            " % complete.",
            sep = ""            
        )
    )
    
}


## ----------------------------------------------------------------------------

###############################################################################

## I am giving an output ------------------------------------------------------

writeLines(
    my_output,
    con = gsub(
        ".in",
        "-out.in",
        gsub("(.*\\\\)(.*)", "\\2", my_input)
    )
)


