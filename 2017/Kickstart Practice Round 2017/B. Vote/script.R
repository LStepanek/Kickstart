## I am loading input data ----------------------------------------------------

my_input <- file.choose()
my_data <- readLines(my_input)


## ----------------------------------------------------------------------------

###############################################################################

## I am changing a working directory ------------------------------------------

setwd(
    gsub("(.*\\\\)(.*)", "\\1", my_input)
)


## ----------------------------------------------------------------------------

###############################################################################

## I am setting decimal digits precision --------------------------------------

options(digits = 22)


## ----------------------------------------------------------------------------

###############################################################################

## helper functions -----------------------------------------------------------

solveTheCase <- function(
    
    my_case
    
){
    
    # '''
    # Returns a solution for one case of the input data.
    # '''
    
    N <- as.integer(strsplit(my_case, split = " ")[[1]][1])
    M <- as.integer(strsplit(my_case, split = " ")[[1]][2])
    
    my_grid <- matrix(rep(0, (M + 1) * (N + 1)), nrow = M + 1)
    
    my_grid[1, ] <- rep(1, N + 1)
    
    if(M > 0){
        
        for(i in 2:(M + 1)){
            
            for(j in 1:(N + 1)){
                
                if(j > i){
                    
                    my_grid[i, j] <- my_grid[i - 1, j] + my_grid[i, j - 1]
                    
                }
                
            }
            
        }        
        
    }
    
    return(my_grid[M + 1, N + 1] / choose(M + N, N))
        
}


## ----------------------------------------------------------------------------

###############################################################################

## flow control ---------------------------------------------------------------

if(
    as.integer(my_data[1]) != length(my_data) - 1
){
    stop("Number of cases is fishy.", call. = FALSE)
}


## ----------------------------------------------------------------------------

###############################################################################

## core computations ----------------------------------------------------------

my_output <- NULL

for(i in 1:as.integer(my_data[1])){

    my_case <- my_data[i + 1]

    my_output <- c(
        my_output,
        paste(
            "Case #",
            i,
            ": ",
            format(round(
                solveTheCase(my_case),
                digits = 8), nsmall = 8),
            sep = ""
        )
    )
    
    flush.console()
    print(
        paste(
            "Process is ",
            format(
                round(
                    i / as.integer(my_data[1]) * 100,
                    digits = 2
                ),
                nsmall = 2
            ),
            " % complete.",
            sep = ""            
        )
    )
    
}


## ----------------------------------------------------------------------------

###############################################################################

## I am giving an output ------------------------------------------------------

writeLines(
    my_output,
    con = gsub(
        ".in",
        "-out.in",
        gsub("(.*\\\\)(.*)", "\\2", my_input)
    )
)


